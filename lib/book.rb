class Book 
    def initialize(title) 
        @title = title 
    end 
    def title 
        @title 
    end 
    def author=(author)
        @author = author 
    end 
    def author 
        @author 
    end 
    def pagecount=(page_count)
        @pagecount = page_count 
    end 
    def pagecount 
        @pagecount 
    end 
    def genre=(genre)
        @genre = genre 
    end 
    def genre 
        @genre 
    end 
end 

